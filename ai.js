    async function sendMessage() {
        const rawText = input.value.trim();
        if (rawText === "") return;
 
        // Тыйым салынған сөздерді тексеру
        const lowerText = rawText.toLowerCase();
        for (let word of bannedWords) {
            if (lowerText.includes(word)) {
                appendMessage("⚠️ Жүйе қауіпсіздігі: Хабарламаңызда жіберуге рұқсат етілмеген немесе тақырыптан тыс сөздер анықталды.", "Warning");
                input.value = "";
                return;
            }
        }
 
        appendMessage(rawText, "User");
        input.value = "";
        sendBtn.disabled = true;
 
        const loadingId = appendMessage(
            "<i class='fa-solid fa-spinner fa-spin'></i> Тарих беттерін ақтарып жатырмын...",
            "Loading"
        );
 
        try {
            const botResponse = await findAnswer(rawText);
            removeMessage(loadingId);
            appendMessage(botResponse, "System");
        } catch (e) {
            removeMessage(loadingId);
            appendMessage("Кешіріңіз, жауап алу мүмкін болмады. Қайта сұрап көріңіз.", "System");
        } finally {
            sendBtn.disabled = false;
        }
    }
 
